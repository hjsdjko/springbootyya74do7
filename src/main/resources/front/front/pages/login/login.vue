<template>
	<view class="content">
		<view class="box" :style='{"width":"inherit","position":"relative","background":"url(http://codegen.caihongy.cn/20231222/5895840a94a04d49bfdd4440ab285c22.gif) no-repeat center top / 100% auto,#fff","height":"inherit"}'>
			<view :style='{"padding":"0 0 0 40rpx","top":"330rpx","left":"0","bottom":"0","background":"url(\"http://codegen.caihongy.cn/20231224/65c44a78b9684f42b8a415dc818a5770.png\") 0% 0% / cover no-repeat ","width":"100%","position":"absolute","height":"auto"}'>
				<image :style='{"width":"140rpx","margin":"120rpx  0  80rpx 0","borderRadius":"20rpx","display":"block","height":"140rpx"}' src="http://codegen.caihongy.cn/20201114/7856ba26477849ea828f481fa2773a95.jpg" mode="aspectFill"></image>
				<view v-if="loginType==1" :style='{"width":"100%","margin":"0 0 20rpx 0","display":"flex","height":"auto"}' class="uni-form-item uni-column">
					<view :style='{"padding":"0","color":"#A2A2A2","textAlign":"left","width":"200rpx","lineHeight":"88rpx","fontSize":"28rpx","fontWeight":"400"}' class="label">账号：</view>
					<input v-model="username" :style='{"padding":"0px","margin":"0 0 0 -20rpx","borderColor":"#000000","color":"#A2A2A2","flex":"1","background":"transparent","borderWidth":"0 0 2rpx  0","fontSize":"28rpx","borderStyle":"solid","height":"88rpx","maxWidth":"60%"}' type="text" class="uni-input" name="" placeholder="请输入账号" />
				</view>
				<view v-if="loginType==1" :style='{"width":"100%","margin":"0 0 20rpx 0","display":"flex","height":"auto"}' class="uni-form-item uni-column">
					<view :style='{"padding":"0","color":"#A2A2A2","textAlign":"left","width":"200rpx","lineHeight":"88rpx","fontSize":"28rpx","fontWeight":"400"}' class="label">密码：</view>
					<input v-model="password" :style='{"padding":"0px","margin":"0 0 0 -20rpx","borderColor":"#000000","color":"#A2A2A2","flex":"1","background":"transparent","borderWidth":"0 0 2rpx  0","fontSize":"28rpx","borderStyle":"solid","height":"88rpx","maxWidth":"60%"}' type="password" class="uni-input" name="" placeholder="请输入密码" />
				</view>
				<view v-if="roleNum>1" :style='{"width":"87%","margin":"0","display":"flex","height":"auto"}'>
					<view :style='{"padding":"0","color":"#A2A2A2","textAlign":"left","width":"200rpx","lineHeight":"88rpx","fontSize":"28rpx","fontWeight":"400"}' class="label">用户类型：</view>
					<picker @change="optionsChange" :value="index" :range="options" :style='{"borderColor":"#000","margin":"0 0 0 -20rpx ","color":"#A2A2A2","flex":"1","borderWidth":"0 0 2rpx 0","lineHeight":"88rpx","fontSize":"28rpx","borderStyle":"solid","fontWeight":"400"}'>
						<view class="uni-picker-type">{{options[index]}}</view>
					</picker>
				</view>
				

				
				<button v-if="loginType==1" class="btn-submit" @tap="onLoginTap" type="primary" :style='{"border":"0","padding":"0px","margin":"40rpx","color":"rgb(255, 255, 255)","borderRadius":"90rpx","background":"linear-gradient(to right, #2DF1FD, #2148D9)","width":"85%","letterSpacing":"2rpx","lineHeight":"90rpx","fontSize":"36rpx","fontWeight":"900","height":"90rpx"}'>登陆</button>
				<button v-if="loginType==2" class="btn-submit" @tap="onFaceLoginTap" type="primary" :style='{"border":"0","padding":"0px","margin":"40rpx","color":"rgb(255, 255, 255)","borderRadius":"90rpx","background":"linear-gradient(to right, #2DF1FD, #2148D9)","width":"85%","letterSpacing":"2rpx","lineHeight":"90rpx","fontSize":"36rpx","fontWeight":"900","height":"90rpx"}'>人脸识别登录</button>
				<view class="links" :style='{"width":"100%","padding":"0 40rpx 180rpx 0px","flexWrap":"wrap","display":"flex","height":"auto"}'>
					<view class="link-highlight" @tap="onRegisterTap('yonghu')" :style='{"padding":"20rpx 40rpx","margin":"0 0 20rpx 40rpx","color":"#fff","borderRadius":"66rpx","background":"linear-gradient(to right, #2DF1FD, #2148D9)","fontSize":"28rpx","fontWeight":"400","order":"1"}'>注册用户</view>
				</view>
				
				<view class="idea1" :style='{"width":"100%","background":"red","display":"none","height":"80rpx"}'>idea1</view>
